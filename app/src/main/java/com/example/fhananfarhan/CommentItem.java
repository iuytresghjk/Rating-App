package com.example.fhananfarhan;

public class CommentItem {
    private String id;
    private int rating;
    private String review;
    private String created_at;
    private String uid;

    // Getter methods
    public String getId() {
        return id;
    }

    public int getRating() {
        return rating;
    }

    public String getReview() {
        return review;
    }

    public String getCreated_at() {
        return created_at;
    }

    public String getUid() {
        return uid;
    }
}
