package com.example.fhananfarhan;

public class RatingItem {
    private int id;
    private int rating;
    private String review;
    private String created_at;

    public int getId() {
        return id;
    }

    public int getRating() {
        return rating;
    }

    public String getReview() {
        return review;
    }

    public String getCreated_at() {
        return created_at;
    }
}
